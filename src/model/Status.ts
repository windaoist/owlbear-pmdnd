export function getStatus(name: string): Status | undefined {
  return StatusList.find((a) => a.name == name)?.duplicate()
}

export function statusStack(name: string, stack: number): Status | undefined {
  const s = getStatus(name)
  if (s == undefined || stack <= 0) {
    return undefined
  }
  s.stack = stack
  return s
}

export function getChildStatus(name: string): Status | undefined {
  const s = getStatus(name)
  if (s == undefined) {
    return undefined
  }
  return getStatus(s.childName)
}

export function getParentStatus(name: string): Status | undefined {
  const s = getStatus(name)
  if (s == undefined) {
    return undefined
  }
  return getStatus(s.parentName)
}
